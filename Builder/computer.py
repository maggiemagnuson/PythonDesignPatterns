class Computer(object):

    def display(self):
        print("Custom Computer")
        print(f"\tCase: {self.case}")
        print(f"\tMainboard: {self.mainboard}")
        print(f"\tCPU: {self.cpu}")
        print(f"\tMemory: {self.memory}")
        print(f"\tHard drive: {self.hard_drive}")
        print(f"\tVideo card: {self.video_card}")